//----------------------------------------------------------------------------------------------------------------------
/// MapService
///
/// @module
//----------------------------------------------------------------------------------------------------------------------

import ol from 'openlayers';

//----------------------------------------------------------------------------------------------------------------------

class MapService {
    constructor()
    {
        this.map = new ol.Map({
            layers: [
                new ol.layer.Tile({
                    preload: Infinity,
                    source: new ol.source.OSM()
                })
            ],
            controls: [],
            view: new ol.View({
                center: [0, 0],
                zoom: 2,
                maxZoom: 20
            })
        });
    } // end constructor
    
    setTarget(target)
    {
        this.map.setTarget(target);
    } // end setTarget
} // end MapService

//----------------------------------------------------------------------------------------------------------------------

export default new MapService();

