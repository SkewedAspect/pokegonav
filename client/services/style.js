//----------------------------------------------------------------------------------------------------------------------
/// StyleService
///
/// @module
//----------------------------------------------------------------------------------------------------------------------

import _ from 'lodash';
import ol from 'openlayers';

//----------------------------------------------------------------------------------------------------------------------

class StyleService {
    constructor()
    {
        this.pokemonStyle = _.map(_.range(152), (id) =>
        {
            return new ol.style.Style({
                image: new ol.style.Icon({
                    anchor: [0.5, 0.5],
                    src: `/static/icons/${ id }.png`
                })
            });
        });
    } // end constructor
    
    getPokeStyle(id)
    {
        return this.pokemonStyle[parseInt(id)];
    } // end getPokeStyle
} // end StyleService

//----------------------------------------------------------------------------------------------------------------------

export default new StyleService();

